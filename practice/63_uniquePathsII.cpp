//my solution -- using backtracking but too slow
void backtrack(vector<vector<int>>& obstacleGrid, int& count, int i, int j){
    if (i > obstacleGrid.size()-1 || j > obstacleGrid[0].size()-1 || obstacleGrid[i][j]==1){
        return;
    }
    else if(i == obstacleGrid.size()-1 && j == obstacleGrid[0].size()-1){
        count++;
        return;
    }
    
    backtrack(obstacleGrid, count, i+1, j);
    backtrack(obstacleGrid, count, i, j+1);
}
int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
    int count = 0, i = 0, j = 0;
    backtrack(obstacleGrid, count, i, j);
    return count;
}

//chat gpt, using DFS --> the key is that you can memorize how many valid path there are starting
// from a specific cell. 
class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        int m = obstacleGrid.size();
        int n = obstacleGrid[0].size();
        vector<vector<int>> memo(m, vector<int>(n, -1));
        return dfs(obstacleGrid, memo, 0, 0);
    }

    int dfs(vector<vector<int>>& obstacleGrid, vector<vector<int>>& memo, int i, int j) {
        if (i >= obstacleGrid.size() || j >= obstacleGrid[0].size() || obstacleGrid[i][j] == 1) 
            return 0;
        if (i == obstacleGrid.size() - 1 && j == obstacleGrid[0].size() - 1) 
            return 1;
        if (memo[i][j] != -1) 
            return memo[i][j];

        memo[i][j] = dfs(obstacleGrid, memo, i + 1, j) + dfs(obstacleGrid, memo, i, j + 1);
        return memo[i][j];
    }
};


